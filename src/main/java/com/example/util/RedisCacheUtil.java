    /**
     * 获取list缓存的内容
     *
     * @param key   键
     * @param start 开始
     * @param end   结束 0 到 -1代表所有值
     *              <p>
     *              QuestionInfoDTO questionInfoDTO = new QuestionInfoDTO();
     *              questionInfoDTO.setId(question.getId());
     *              questionInfoDTO.setAnswer(question.getAnswer());
     *              questionInfoDTO.setUserAnswer(userAnswers);
     *              questionInfoDTO.setOptions(question.getOptions());
     *              questionInfoDTO.setTime(new Date());
     *              私有 公开 已购买 管理与 参与者 拥有者
     * @return
     */
    public <T> List<T> lGet(String key, long start, long end) {
        List<Object> list = redisTemplate.opsForList().range(key, start, end);
        List<T> res = new ArrayList<>();
        for (Object o : list) {
            res.add((T) o);
        }
        try {
            return res;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }



